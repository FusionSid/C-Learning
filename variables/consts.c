#include <stdio.h>

int main()
{   
    /* Constants: */

    /* 
    To make a constant in C we use the constant keyword
    Also its good practice to name the variable in caps
    A constant variable is a constant so it cant be changed
    */
    const float price = 69.420;
    
    // Printing it
    printf("Price: %f", price);

    return 0;
}